// for loop

for (let i = 0; i < 10; i++) {
    console.log(i);
    console.log(`For loop number: ${i}`);
}

// while loop

let i = 0;
while (i < 10) {
    console.log(`While loop number: ${i}`);
    i++;
}

// do while loop

let j = 0;
do {
    console.log(`Do while loop number: ${j}`);
    j++;
}
while (j > 10);
