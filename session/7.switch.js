// switch statement

count = "one";

switch (count) {
    case "one":
        console.log("count is 1");
        break;
    case "two":
        console.log("count is 2");
        break;
    case "three":
        console.log("count is 3");
        break;
    default:
        console.log("count is not 1, 2 or 3");
        break;
}